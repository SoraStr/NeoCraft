/// Read a Java .properties file into a HashMap.
/// Preserves only key-value pairs; comments and blank lines are not stored.
pub async fn read_properties(path: &Path) -> Result<HashMap<String, String>, std::io::Error> {
    let file = tokio::fs::File::open(path).await?;
    let reader = BufReader::new(file);
    let mut lines = reader.lines();
    let mut props = HashMap::new();

    loop {
        let line = match lines.next_line().await {
            Ok(Some(l)) => l,
            Ok(None) => break,
            Err(e) => return Err(e),
        };
        let trimmed = line.trim();

        // Skip comments and blank lines
        if trimmed.is_empty() || trimmed.starts_with('#') || trimmed.starts_with('!') {
            continue;
        }

        // Split on first = or :
        if let Some(pos) = trimmed.find(|c: char| c == '=' || c == ':') {
            let key = trimmed[..pos].trim().to_string();
            let value = trimmed[pos + 1..].trim().to_string();
            if !key.is_empty() {
                props.insert(key, value);
            }
        }
    }

    Ok(props)
}

/// Write a HashMap to a .properties file, preserving existing comments and structure.
/// Keys that exist in the file are updated in-place; new keys are appended.
pub async fn write_properties(
    path: &Path,
    props: &HashMap<String, String>,
) -> Result<(), std::io::Error> {
    // Read original file lines to preserve comments and ordering
    let original_lines: Vec<String> = if path.exists() {
        let content = tokio::fs::read_to_string(path).await?;
        content.lines().map(|s| s.to_string()).collect()
    } else {
        Vec::new()
    };

    let mut used_keys = std::collections::HashSet::new();
    let mut output = String::new();

    for line in &original_lines {
        let trimmed = line.trim();
        if trimmed.is_empty() || trimmed.starts_with('#') || trimmed.starts_with('!') {
            // Preserve comments and blank lines as-is
            output.push_str(line);
            output.push('\n');
        } else if let Some(pos) = trimmed.find(|c: char| c == '=' || c == ':') {
            let key = trimmed[..pos].trim();
            if let Some(new_value) = props.get(key) {
                // Update existing key with new value
                output.push_str(&format!("{}={}\n", key, new_value));
                used_keys.insert(key.to_string());
            }
            // If key is not in props, omit it (supports deletion)
        } else {
            // Malformed line — preserve as-is
            output.push_str(line);
            output.push('\n');
        }
    }

    // Append new keys that weren't in the original file
    for (key, value) in props {
        if !used_keys.contains(key.as_str()) {
            output.push_str(&format!("{}={}\n", key, value));
        }
    }

    tokio::fs::write(path, output).await?;
    Ok(())
}

/// Recursively copy a directory and all its contents from `src` to `dst`.
/// Creates `dst` if it doesn't exist. Skips symlinks for safety.
pub async fn copy_dir_all(src: &Path, dst: &Path) -> std::io::Result<()> {
    tokio::fs::create_dir_all(dst).await?;
    let mut entries = tokio::fs::read_dir(src).await?;
    while let Some(entry) = entries.next_entry().await? {
        let file_type = entry.file_type().await?;
        let src_path = entry.path();
        let dst_path = dst.join(entry.file_name());
        if file_type.is_dir() {
            Box::pin(copy_dir_all(&src_path, &dst_path)).await?;
        } else if file_type.is_symlink() {
            tracing::warn!(path = %src_path.display(), "Skipping symlink in imported directory");
        } else {
            tokio::fs::copy(&src_path, &dst_path).await?;
        }
    }
    Ok(())
}

/// Recursively copy a directory while emitting progress events.
pub async fn copy_dir_all_with_progress(
    src: &Path,
    dst: &Path,
    task_id: String,
    event_tx: &tokio::sync::broadcast::Sender<Event>,
) -> std::io::Result<u64> {
    emit_copy_progress(event_tx, &task_id, 0, 0);
    let mut copied = 0;
    let mut discovered_total = 0;

    let mut last_emit = Instant::now();
    copy_dir_all_progress_inner(
        src,
        dst,
        &task_id,
        event_tx,
        &mut discovered_total,
        &mut copied,
        &mut last_emit,
    )
    .await?;

    emit_copy_progress(event_tx, &task_id, copied, discovered_total);
    Ok(copied)
}

async fn copy_dir_all_progress_inner(
    src: &Path,
    dst: &Path,
    task_id: &str,
    event_tx: &tokio::sync::broadcast::Sender<Event>,
    discovered_total: &mut u64,
    copied: &mut u64,
    last_emit: &mut Instant,
) -> std::io::Result<()> {
    tokio::fs::create_dir_all(dst).await?;
    let mut entries = tokio::fs::read_dir(src).await?;
    while let Some(entry) = entries.next_entry().await? {
        let file_type = entry.file_type().await?;
        let src_path = entry.path();
        let dst_path = dst.join(entry.file_name());
        if file_type.is_dir() {
            Box::pin(copy_dir_all_progress_inner(
                &src_path,
                &dst_path,
                task_id,
                event_tx,
                discovered_total,
                copied,
                last_emit,
            ))
            .await?;
        } else if file_type.is_symlink() {
            tracing::warn!(path = %src_path.display(), "Skipping symlink in imported directory");
        } else if !file_type.is_file() {
            tracing::warn!(path = %src_path.display(), "Skipping non-regular file in imported directory");
        } else {
            let size = entry.metadata().await?.len();
            *discovered_total = discovered_total.saturating_add(size);
            if last_emit.elapsed() >= Duration::from_millis(100) {
                emit_copy_progress(event_tx, task_id, *copied, *discovered_total);
                *last_emit = Instant::now();
            }
            copy_file_with_progress(
                &src_path,
                &dst_path,
                task_id,
                event_tx,
                *discovered_total,
                copied,
                last_emit,
            )
            .await?;
        }
    }
    Ok(())
}

async fn copy_file_with_progress(
    src: &Path,
    dst: &Path,
    task_id: &str,
    event_tx: &tokio::sync::broadcast::Sender<Event>,
    total: u64,
    copied: &mut u64,
    last_emit: &mut Instant,
) -> std::io::Result<()> {
    let mut input = tokio::fs::File::open(src).await?;
    let mut output = tokio::fs::File::create(dst).await?;
    let mut buffer = vec![0_u8; 1024 * 1024];
    let mut emitted_first_chunk = false;

    loop {
        let read = input.read(&mut buffer).await?;
        if read == 0 {
            break;
        }

        output.write_all(&buffer[..read]).await?;
        *copied = copied.saturating_add(read as u64);

        if !emitted_first_chunk || last_emit.elapsed() >= Duration::from_millis(100) {
            emit_copy_progress(event_tx, task_id, *copied, total);
            *last_emit = Instant::now();
            emitted_first_chunk = true;
        }
    }

    output.flush().await?;
    Ok(())
}

fn emit_copy_progress(
    event_tx: &tokio::sync::broadcast::Sender<Event>,
    task_id: &str,
    downloaded: u64,
    total: u64,
) {
    let percent = if total > 0 {
        (downloaded as f64 / total as f64 * 100.0).min(100.0)
    } else {
        0.0
    };
    let _ = event_tx.send(Event::DownloadProgress {
        task_id: task_id.to_string(),
        downloaded,
        total,
        percent,
        phase: Some("import".into()),
        status: Some("copying".into()),
    });
}
